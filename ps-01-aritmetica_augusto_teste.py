# Entrada dos numeros
a = int(input())
b = int(input())

# Printar as saidas
print("a =", a)             # primeiro numero
print("b =", b)             # segundo numero
print("a + b =", a+b)       # soma
print("a - b =", a-b)       # subtracao
print("a * b =", a*b)       # multiplicacao
print("a // b =", a//b)     # divisao inteira
print("a % b =", a%b)       # resto da divisao
