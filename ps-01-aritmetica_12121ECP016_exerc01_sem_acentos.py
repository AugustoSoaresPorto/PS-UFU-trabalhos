# Entrada:
# * Perguntar e armazenar os valores das variaveis a e b utilizando o input()
# * Considerar apenas numeros inteiros ao usar o comando int().
# * Nao escrever nada nos parenteses do input(), para que o resultado na saida seja o esperado.
a = int(input())
b = int(input())

# Processamento e saida:
# * Efetuar as operacoes indicadas na questao.
# * Imprimir os valores das variaveis e das equacoes, em seguida, utilizando a funcao print().
print('a = {}'.format(a))
print('b = {}'.format(b))
print('a + b = {}'.format(a + b))
print('a - b = {}'.format(a - b))
print('a * b = {}'.format(a * b))
print('a // b = {}'.format(a // b))
print('a % b = {}'.format(a % b))

